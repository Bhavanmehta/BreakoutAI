"""Live "what would this strategy do TODAY" suggestions for the four curated
options strategies shown on options_strategies.html.

The strategy parameters here are copied VERBATIM from the backtest definitions
so the live suggestion matches what was actually tested:

  - IC-1pct-manage50            iron_condor.py     ICStrategy(0.010, 0.008, entry_offset_days=2, target_frac=0.50, sl_mult=2.0)
  - Momentum-Expiry-HoldToSettle backtest.py       Strategy("momentum3", 0.012, 0.008, entry_offset_days=2)  held to settlement
  - MeanReversion-Expiry         backtest.py       Strategy("mean_rev", 0.015, 0.010, sl_mult=1.5, target_frac=0.6, entry_offset_days=2)
  - FlowExpiry-pcr-Medium        round3_followups  sig_pcr (hi=1.3, lo=0.7) x (0.012, 0.008), sl_mult=1.5, target_frac=0.6

Signals (same maths as backtest.py / zen_sweep.py, computed on live data):
  momentum3: 3-day close return, deadband 0.0005 -> bull/bear/skip
  mean_rev : 1-day close return, fade moves > 0.6% (rally -> bear, drop -> bull)
  pcr      : chain-wide put OI / call OI; > 1.3 crowded puts -> bull, < 0.7 -> bear
  IC       : non-directional, always quotes both sides.

Spread construction mirrors backtest.py build_spread():
  bull -> put credit spread BELOW spot  (short spot*(1-otm), long spot*(1-otm-wing))
  bear -> call credit spread ABOVE spot (short spot*(1+otm), long spot*(1+otm+wing))
  IC   -> both a call spread above and a put spread below.

GET /api/options_strategies?symbol=NIFTY
Serves suggestions only; the page reads backtest stats from the static
data/options_strategies.json (regenerated by options_backtest/scripts/export_web_json.py).
"""
